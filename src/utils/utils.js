const reg = /(((^https?:(?:\/\/)?)(?:[-;:&=\+\$,\w]+@)?[A-Za-z0-9.-]+|(?:www.|[-;:&=\+\$,\w]+@)[A-Za-z0-9.-]+)((?:\/[\+~%\/.\w-_]*)?\??(?:[-\+=&;%@.\w_]*)#?(?:[\w]*))?)$/g;

export function isUrl(path) {
  return reg.test(path);
}

// 移除属性
export const removeAttFromObj = (obj, keyArray) => {
  if (obj) {
    if (keyArray && keyArray.length > 0) {
      const result = {};
      for (const [k, v] of Object.entries(obj)) {
        if (!keyArray.includes(k)) {
          result[k] = v;
        }
      }
      return result;
    }
    return obj;
  }
};

export const addPagnationParams = data => {
  let res = {
    ...data,
  };

  if (data && !data.pagnationRes) {
    res.limit = data.pageSize || 10;
    res.start = data.page > 1 ? (data.page - 1) * data.pageSize : 0;
  }

  res = removeAttFromObj(res, ['pageSize', 'page', 'pagnationRes']);

  return res;
};

export const toFixed = (val, type = false) => {
  if (Object.prototype.toString.call(val) === '[object Number]') {
    let res = parseFloat(val);
    if (type) {
      res /= 1000;
      return res > 0 ? `${res.toFixed(3)} s` : res.toFixed(2);
    } else {
      return `${res.toFixed(2)} ms`;
    }
  }

  return '未知';
};

export const limitTo = (value, num) => {
  if (!value) return;
  let text = '';
  if (value.length < num) {
    text = value;
  } else {
    text = `${value.substring(0, num)}···`;
  }
  return text;
};

export const toSize = val => {
  const res = val * 1;
  if (res >= 1024) {
    return `${(res / 1024).toFixed(2)} KB`;
  } else if (res > 0) {
    return `${res.toFixed(2)} B`;
  } else {
    return 0;
  }
};
